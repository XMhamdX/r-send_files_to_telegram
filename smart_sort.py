# عبارات الصدارة المطلقة (Priority -1)
ABSOLUTE_FIRST_PHRASES = ['تعريف عام', 'مقدمة عامة', 'فيديو تعريفي']

def natural_keys(text):
    """
    يقوم بتقسيم النص إلى (أرقام) و (نصوص) لفرزها بشكل طبيعي (Natural Sort)
    مثال: "2" يسبق "10" 
    """
    return [int(c) if c.isdigit() else c for c in re.split(r'(\d+)', text)]

def smart_sort_key(filename):
    """
    تُرجع: (الأولوية, مفتاح_الفرز_الطبيعي)
    الأولوية -1 = كلمات صدارة مطلقة
    الأولوية 0 = باقي الملفات مرتبة ترتيباً طبيعياً تصاعدياً 
    """
    name_without_ext = os.path.splitext(filename)[0]
    cleaned_name = re.sub(r'[_.\-]', ' ', name_without_ext).strip()

    # 1. صدارة مطلقة؟
    if any(phrase in cleaned_name for phrase in ABSOLUTE_FIRST_PHRASES):
        return (-1, natural_keys(cleaned_name))

    # 2. تحويل الكلمات العربية الرقمية إلى أرقام صريحة في النص
    # نرتب الكلمات تنازلياً حسب الطول لتجنب استبدال الكلمات القصيرة داخل الطويلة إن وجدت
    sorted_words = sorted(NUMERAL_WORDS.keys(), key=len, reverse=True)
    for word in sorted_words:
        # استخدام التعبيرات النمطية لضمان استبدال الكلمة فقط إذا كانت مستقلة
        cleaned_name = re.sub(rf'\b{word}\b', str(NUMERAL_WORDS[word]), cleaned_name)

    # 3. معرفة هل الاسم يحتوي على أرقام (سواء أرقام حقيقية أو كلمات تحولت لأرقام)
    has_numbers = any(char.isdigit() for char in cleaned_name)

    # 4. إرجاع النتيجة
    # الأولوية 0 = ملفات حرة (لا تحتوي على أي رقم) تُرتب أبجدياً/زمنياً بعد الصدارة مباشرة
    # الأولوية 1 = ملفات مرقمة (تُرتب باستخدام الفرز الطبيعي Natural Sort)
    if has_numbers:
        return (1, natural_keys(cleaned_name))
    else:
        return (0, natural_keys(cleaned_name))
