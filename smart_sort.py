# دمج القواميس للبحث المباشر السريع
ALL_NUMERALS = {**UNITS, **TENS, **HUNDRED}

def parse_arabic_number(text):
    """تحليل النص العربي واستخراج القيمة الرقمية المركبة"""
    # 1. البحث عن تشكيلة (آحاد + عشرات) مثل "السادس والأربعون"
    for unit_word, unit_val in sorted(UNITS.items(), key=lambda x: len(x[0]), reverse=True):
        for ten_word, ten_val in sorted(TENS.items(), key=lambda x: len(x[0]), reverse=True):
            if unit_val > 9:
                continue
            
            # نمط: "السادس والأربعون" أو "سادس والأربعين" أو "سادس واربعين"
            # تم إضافة مسافات اختيارية حول الواو للتعامل مع "الحادية والعشرون" بشكل صحيح
            pattern1 = f"{unit_word} و {ten_word}"
            pattern2 = f"{unit_word} وال{ten_word}"
            pattern3 = f"{unit_word} و{ten_word}"
            pattern4 = f"{unit_word}وال{ten_word}" # مثل: الحاديةوالعشرون لو كانت متصلة بسبب التشكيل
            
            if any(p in text for p in [pattern1, pattern2, pattern3, pattern4]):
                return unit_val + ten_val

    # 2. إذا لم يكن تركيباً، نبحث ككلمة مفردة في كل القاموس
    for key, val in sorted(ALL_NUMERALS.items(), key=lambda x: len(x[0]), reverse=True):
        if key in text:
            return val
            
    return None

def smart_sort_key(filename):
    """
    دالة فرز ذكية: تبحث عن الأرقام العادية أولاً، 
    إذا لم تجدها تبحث عن الكلمات المعبرة عن الأرقام وتجمع التراكيب.
    """
    name_without_ext = os.path.splitext(filename)[0]
    cleaned_name = re.sub(r'[_.\-]', ' ', name_without_ext)
    
    # محاولة استخراج الرقم من نهاية الاسم
    numbers = re.findall(r'\d+', name_without_ext)
    
    if numbers:
        # فصل الاسم عن الرقم ليتجمعوا مع بعضهم
        base_name = re.sub(r'\s*\d+$', '', cleaned_name).strip()
        return (base_name, int(numbers[-1])) # نأخذ آخر رقم كعامل الترتيب
        
    # 2. تحليل الكلمات العربية المعبرة عن الأرقام (مفردة ومركبة)
    val = parse_arabic_number(cleaned_name)
    if val is not None:
        # إزالة الكلمة الرقمية لتجميع الأسماء المشتركة
        # هذا تقريب، لكنه يفي بالغرض حالياً
        return (cleaned_name, val)
            
    # 3. إذا لم يجد شيئاً، يفرز بالاسم نفسه مع إعطائه قيمة لانهاية ليأتي بعد الأرقام
    return (cleaned_name, float('inf'))
